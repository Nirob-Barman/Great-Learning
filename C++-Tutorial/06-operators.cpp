#include <iostream>

using namespace std;

int main()
{
    int a = 15, b = 10;
    cout << "The Summation is: " << a + b << endl;
    cout << "The Subtraction is: " << a - b << endl;
    cout << "The Multiplication is: " << a * b << endl;
    cout << "The Division is: " << a / b << endl;
    cout << "The Division is: " << b / a << endl;
    cout << "The Modulo is: " << a % b << endl;
    return 0;
}