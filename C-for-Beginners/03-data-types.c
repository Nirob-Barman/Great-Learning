#include <stdio.h>

int main()
{
    int i = 100;
    printf("Value of i is = %d\n", i);

    float f = 45.65;
    printf("Value of f is = %f\n", f);
    printf("Value of f is = %0.2f\n", f);

    double d = 50.456;
    printf("Value of d is = %lf\n", d);

    char s = 'A';
    printf("Value of s is %c\n", s);
    return 0;
}